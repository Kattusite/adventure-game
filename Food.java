public class Food{
    private String name;
    private int health;
    private int risk;
    public Food(String name, int health, int risk){
        this.name = name;
        this.health= health;
        this.risk = risk;
    }
public String name(){
    return name;
}
public int health(){
    return health;
}
public int risk(){
    return risk;
}
}
